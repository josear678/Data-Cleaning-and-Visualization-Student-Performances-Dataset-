import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
df = pd.read_csv("https://raw.githubusercontent.com/josear678/BCA_Summer_2025/refs/heads/main/student-mat.csv",sep=';')

df2 = df

df2['Medu'] = df2['Medu'].replace(to_replace={0:'none',1:'primary education(4th grade)',2:'5th to 9th grade',3:'secondary education',4:'higher education'})
df2['Fedu'] = df2['Fedu'].replace(to_replace={0:'none',1:'primary education(4th grade)',2:'5th to 9th grade',3:'secondary education',4:'higher education'})
df2['traveltime'] = df2['traveltime'].replace(to_replace={1:'<15min',2:'15 to 30 min',3:'30 min to 1 hour',4:'>1hour'})
df2['studytime'] = df2['studytime'].replace(to_replace={1:'<2 hours',2:'2 to 5 hours',3:'5 to 10 hours',4:'>10 hours'})
df2['famrel'] = df2['famrel'].replace(to_replace={1:'very bad', 2:'bad', 3:'neutral',4:'good',5:'excellent'})
df2['goout'] = df['goout'].replace(to_replace={1:'never',2:'barely',3:'sometimes',4:'often',5:'very often'})
df2['Dalc'] = df['Dalc'].replace(to_replace={1:'very low',2:'low',3:'mid',4:'high',5:'very high'})
df2['Walc'] = df['Walc'].replace(to_replace={1:'very low',2:'low',3:'mid',4:'high',5:'very high'})
df2['health'] = df['health'].replace(to_replace={1:'very bad',2:'bad',3:'mid',4:'good',5:'very good'})
df2['freetime'] = df['freetime'].replace(to_replace={1:'very low',2:'low',3:'mid',4:'high',5:'very high'})

df2 = pd.get_dummies(df2, dtype=int)
